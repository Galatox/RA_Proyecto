void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance == TIM1){
		static int ix = 0;
		// BT: 100 Hz
		// Acquisition of the values of the motor that the encoder gives us
		previousCount = count;
		count += (int16_t)__HAL_TIM_GET_COUNTER(&htim3);
		speed = (count-previousCount)/0.01;
		speedRPM = speed * 60/861;

		//Start of the feedback loop

		if(controlMode ==  RA_POSITION){
			// Makes the regulation base on a relative position and not an absolute one
			//More details in RAdef.c in RA_inputModeControl()
			posREV = count/861;
			//Position
			// U1
			posStorage[1] = posStorage[0];
			posStorage[0] = Ref-posREV;
			//Y1
			speedStorage[1] = speedStorage[0];
			//Output of the first controller
			speedStorage[0] = RA_PositionController(posStorage);
			//Output of the second controller
			speedStorage[2] = RA_SpeedController(speedStorage);
		}

		if(controlMode == RA_SPEED){
			//Speed
			// U2 y U1
			speedStorage[1] = speedStorage[0];
			speedStorage[0] = Ref-speedRPM;
			//Y1
			speedStorage[2] = RA_SpeedController(speedStorage);
		}

		// Setting of the new values that gave us the regulator
		if(controlMode != RA_DEFAULT){

			//Regulation modes (speed / position wise)
			if((controlMode != RA_STEP) && (controlMode != RA_STOP)){
				TIM4->CCR1 = abs(speedStorage[2]);
				ix++;
				// Print of the speed/position of the motor
				// Take in count the ripple values.
				if(ix > 200){
					controlMode == RA_SPEED ? RA_Print_Speed() : RA_Print_Position();
					ix = 0;
				}
			}

			//Step mode.
			else if(controlMode == RA_STEP){
				TIM4->CCR1 = abs(Ref*RA_MAX_SPEED_CRR/204);
				if(Ref > 0)
					RA_Motor_Forward();
				else if(Ref < 0){
					RA_Motor_Reverse();
				}
				ix++;
				if(ix >200){
					RA_Print_Step();
					ix = 0;
				}
			}
		}

		//End of the feedback loop
		__HAL_TIM_SET_COUNTER(&htim3,0);
	}
}

